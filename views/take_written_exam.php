$allQuestions = [];

try {
  foreach ($distribucion as $topicId => $limitTopic) {
    $topicId = (int) $topicId;
    $limitTopic = (int) $limitTopic;
    if ($limitTopic <= 0) {
      continue;
    }

    // Get sections for this topic
    $stmtSections = $db->prepare("SELECT id_seccion, letra, nombre, descripcion FROM seccion WHERE id_topico = :tid ORDER BY letra");
    $stmtSections->execute([':tid' => $topicId]);
    $sections = $stmtSections->fetchAll();

    if (empty($sections)) {
      continue;
    }

    $questionsTopic = [];
    $sectionsCount = count($sections);
    $limitPerSection = (int) ceil($limitTopic / $sectionsCount);

    foreach ($sections as $sec) {
      $stmt = $db->prepare("
          SELECT p.*, s.letra AS seccion, s.nombre AS seccion_nombre, s.descripcion AS seccion_descripcion, s.id_topico AS parte
          FROM pregunta p
          INNER JOIN seccion s ON p.id_seccion = s.id_seccion
          WHERE s.id_seccion = :sid
          ORDER BY RAND()
          LIMIT :lim
      ");
      $stmt->bindValue(':sid', $sec['id_seccion'], PDO::PARAM_INT);
      $stmt->bindValue(':lim', $limitPerSection, PDO::PARAM_INT);
      $stmt->execute();
      $questions = $stmt->fetchAll();
      foreach ($questions as $q) {
        $questionsTopic[] = $q;
      }
    }

    shuffle($questionsTopic);
    $questionsTopic = array_slice($questionsTopic, 0, $limitTopic);
    $allQuestions = array_merge($allQuestions, $questionsTopic);
  }


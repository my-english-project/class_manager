  // Option selection
  function selectOption(qId, optId) {
    selectedOptionMap[qId] = optId;

    // Enable the confirm button
    const btn = document.getElementById('btn-next-' + qId);
    if (btn) btn.disabled = false;
  }

  // Confirm answer and show immediate feedback
  function confirmAndNext(qId, index) {
    const card = document.getElementById('q-card-' + qId);
    const correctId = parseInt(card.getAttribute('data-correct-id'), 10);
    const correctText = card.getAttribute('data-correct-text');
    const part = card.getAttribute('data-part');
    const section = card.getAttribute('data-section');
    const selectedId = selectedOptionMap[qId];

    const btn = document.getElementById('btn-next-' + qId);

    if (!confirmedSet.has(qId)) {
      // --- STEP 1: CONVERT BUTTON TO SHOW FEEDBACK ---
      confirmedSet.add(qId);

      // Disable all radio buttons
      const radios = card.querySelectorAll('input[type="radio"]');
      radios.forEach(r => r.disabled = true);

      // Disable other options hover
      const labels = card.querySelectorAll('.exam-option-label');
      labels.forEach(l => l.classList.add('disabled'));

      // Check if selected is correct
      const isCorrect = (selectedId === correctId);

      // Color selected and correct labels
      labels.forEach(l => {
        const radio = l.querySelector('input');
        const val = parseInt(radio.value, 10);
        if (val === correctId) {
          l.classList.add('correct');
        } else if (val === selectedId && !isCorrect) {
          l.classList.add('incorrect');
        }
      });

      // Show non-intrusive feedback panel
      const fbPanel = document.getElementById('feedback-' + qId);
      const fbTitle = fbPanel.querySelector('.feedback-title');
      const fbExplanation = fbPanel.querySelector('.feedback-explanation');

      fbPanel.style.display = 'block';

      if (isCorrect) {
        fbPanel.classList.add('success');
        fbTitle.innerHTML = '✨ ¡Correcto!';
      } else {
        fbPanel.classList.add('error');
        fbTitle.innerHTML = '❌ Incorrecto';
      }

      // Fetch grammar explanation
      const key = part + '_' + section;
      const ruleExplanation = explanations[key] || "Explicación gramatical en base a las reglas de modales y condicionales ingleses.";

      fbExplanation.innerHTML = `<b>Respuesta correcta:</b> ${correctText}.<br><br><b>¿Por qué?</b> ${ruleExplanation}`;

      // Change button text
      if (index === totalQuestions - 1) {
        btn.innerText = 'Finalizar Examen';
      } else {
        btn.innerText = 'Siguiente Pregunta';
      }
    } else {
      // --- STEP 2: TRANSITION TO NEXT QUESTION WIZARD ---
      card.style.display = 'none';

      if (index < totalQuestions - 1) {
        // Show next card
        currentIndex = index + 1;
        const nextCardId = document.querySelectorAll('.exam-question-card')[currentIndex].id;
        const nextCard = document.getElementById(nextCardId);
        nextCard.style.display = 'block';

        // Scroll to the top of the next question card with an offset for the sticky header
        setTimeout(() => {
          const headerOffset = 180; // app header + sticky sub-header + comfort buffer
          const elementPosition = nextCard.getBoundingClientRect().top;
          const offsetPosition = elementPosition + window.pageYOffset - headerOffset;

          window.scrollTo({
            top: offsetPosition,
            behavior: 'smooth'
          });
        }, 50);

        // Update progress indicator
        updateProgress();
      } else {
        // End of exam
        sendExamDataDirect();
      }
    }
  }

  // Update progress bar
  function updateProgress() {
    let activeQuestionNum = currentIndex + 1;
    let pct = (activeQuestionNum / totalQuestions) * 100;

    document.getElementById('exam-progress-bar').style.width = pct + '%';
    document.getElementById('progress-text').innerText = 'Pregunta ' + activeQuestionNum + ' de ' + totalQuestions;
  }

  // Timer Logic
  function startTimer() {
    examTimerInterval = setInterval(function () {
      if (timeRemaining <= 0) {
        clearInterval(examTimerInterval);
        timeRemaining = 0;
        autoSubmitOnTimeout();
      } else {
        timeRemaining--;
        let mins = Math.floor(timeRemaining / 60);
        let secs = timeRemaining % 60;
        document.getElementById('exam-timer').innerText =
          (mins < 10 ? '0' : '') + mins + ':' + (secs < 10 ? '0' : '') + secs;

        // Warning color under 5 minutes
        if (timeRemaining < 300) {
          document.getElementById('exam-timer').style.color = '#eb5757';
        }
      }
    }, 1000);
  }

  function autoSubmitOnTimeout() {
    Swal.fire({
      title: '¡Tiempo agotado!',
      text: `El tiempo límite de ${examDurationMinutes} minutos ha finalizado. Tus respuestas se enviarán automáticamente.`,
      icon: 'warning',
      allowOutsideClick: false,
      confirmButtonText: 'Aceptar',
      confirmButtonColor: 'var(--uts-green)'
    }).then(() => {
      sendExamDataDirect();
    });
  }

  // Submit Data via Fetch API
  function sendExamDataDirect() {
    clearUnloadGuard();
    clearInterval(examTimerInterval);

    const form = document.getElementById('written-exam-form');
    const formData = new FormData(form);

    // Append the answers from the JavaScript map so disabled inputs are not lost
    for (const [qId, optId] of Object.entries(selectedOptionMap)) {
      formData.append(`answers[${qId}]`, optId);
    }

    // Show premium Loading Spinner
    Swal.fire({
      title: 'Guardando y calificando...',
      text: 'Por favor, no cierres esta pestaña.',
      allowOutsideClick: false,
      didOpen: () => {
        Swal.showLoading();
      }
    });

    fetch('index.php?action=submit_written_exam', {
      method: 'POST',
      body: formData
    })
      .then(response => response.json())
      .then(data => {
        if (data.success) {
          Swal.fire({
            title: '¡Examen Completado!',
            html: `Tu examen fue calificado automáticamente.<br><br>Aciertos: <b>${data.score} / ${data.total}</b><br>Calificación obtenida: <span style="font-size: 28px; font-weight: 800; color: var(--uts-green);">${data.grade}</span>`,
            icon: 'success',
            confirmButtonColor: 'var(--uts-green)',
            confirmButtonText: 'Regresar al Dashboard'
          }).then(() => {
            window.location.href = 'index.php?page=home';
          });
        } else {
          Swal.fire({
            title: 'Error',
            text: data.message || 'Hubo un error al procesar el examen.',
            icon: 'error',
            confirmButtonColor: '#eb5757',
            confirmButtonText: 'Volver a intentar'
          }).then(() => {
            window.location.reload();
          });
        }
      })
      .catch(error => {
        Swal.fire({
          title: 'Error de Red',
          text: 'No se pudo conectar al servidor. Revisa tu conexión a Internet.',
          icon: 'error',
          confirmButtonColor: '#eb5757',
          confirmButtonText: 'Reintentar'
        });
      });
  }

  // Start official exam on Welcome card click
  let examStarted = false;

  function startOfficialExam() {
    examStarted = true;

    // Hide welcome card, show exam
    document.getElementById('welcome-card').style.display = 'none';
    document.querySelector('.take-exam-wrapper').style.display = 'block';

    // Scroll to the absolute top of the page instantly to prevent layout jump
    window.scrollTo(0, 0);

    // Double check scroll position after rendering to ensure it's at the absolute top
    setTimeout(() => {
      window.scrollTo({
        top: 0,
        behavior: 'auto'
      });
    }, 30);

    // Start timer
    startTimer();

    // Prevent back/forward navigation using popstate history lock
    history.pushState(null, null, location.href);
    window.addEventListener('popstate', function () {
      history.pushState(null, null, location.href);
      Swal.fire({
        title: '⚠️ Navegación Bloqueada',
        text: 'Durante el examen no se permite retroceder ni avanzar en la navegación.',
        icon: 'warning',
        confirmButtonColor: 'var(--uts-green)',
        confirmButtonText: 'Continuar Examen'
      });
    });
  }

  // Window Unload Guard
  window.addEventListener('beforeunload', function (e) {
    if (examStarted && confirmedSet.size < totalQuestions) {
      e.preventDefault();
      e.returnValue = 'Si sales del examen, tus respuestas se perderán y se registrará como no completado. ¿Estás seguro?';
      return e.returnValue;
    }
  });

  // Clear unload event listener on successful complete submit
  function clearUnloadGuard() {
    examStarted = false;
  }
